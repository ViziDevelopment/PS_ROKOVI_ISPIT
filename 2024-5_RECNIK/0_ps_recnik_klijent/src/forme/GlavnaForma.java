    private void popunitabelurecnik() {
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.UCITAJ_RECNIK,null);
        Komunikacija.getInstance().posaljiZahtev(kz);
        
        //
        ServerskiOdgovor so =Komunikacija.getInstance().primiOdgovor();
        List<Rec> lista = (List<Rec>) so.getOdgovor();
        
        ModelTabeleRecnik mtr = new ModelTabeleRecnik(lista);
        jTableRecnik.setModel(mtr);
    }
