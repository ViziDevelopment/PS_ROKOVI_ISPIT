    private void jButtonPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPosaljiActionPerformed
       String unos = jTextFieldPretraga.getText();
       
       KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.PRETRAGA, unos);
        Komunikacija.getInstance().posaljiZahtev(kz);
        
        ServerskiOdgovor so = komunikacija.Komunikacija.getInstance().primiOdgovor();
        Rec r = (Rec) so.getOdgovor();
        if(r==null){
            JOptionPane.showMessageDialog(null, "GRESKA");
             jButtonUnesi.setVisible(true);
        }else{
              List<Prevod> prevodi =r.getPrevodi();
                String rezultat="";
                for (Prevod p : prevodi) {
                    rezultat+=p.getEngRec();
                    rezultat+=";";
                }
                jTextArea1.setText(rezultat);
        }
    }//GEN-LAST:event_jButtonPosaljiActionPerformed

    private void jButtonUnesiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUnesiActionPerformed
       String srp = jTextFieldPretraga.getText(); //macka
       String prevodi = jTextArea1.getText(); //cat;dsda;
       String[] nizPrevoda =  prevodi.split(";"); //{cat,dada}
        
       Rec rec = new Rec();
       rec.setSrpskaRec(srp); //macka
        List<Prevod> lista =  new ArrayList<>();
        for (String el : nizPrevoda) {
            Prevod p = new Prevod();
            p.setEngRec(el);
            p.setRec(rec);
            lista.add(p);
        }
         rec.setPrevodi(lista);
        
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.UNESI, rec);
        Komunikacija.getInstance().posaljiZahtev(kz);
        
        ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
        boolean uspeh = (boolean) so.getOdgovor();
        if(uspeh){
            JOptionPane.showMessageDialog(null, "USPEH PRILIKOM UNOSA");
             popunitabelurecnik();
             jButtonUnesi.setVisible(false);
        } else{
            JOptionPane.showMessageDialog(null, "GRESKA PRILIKOM UNOSA");
        }
       
        
    }//GEN-LAST:event_jButtonUnesiActionPerformed

    

