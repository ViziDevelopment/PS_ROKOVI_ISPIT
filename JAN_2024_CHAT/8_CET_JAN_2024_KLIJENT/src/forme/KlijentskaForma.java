    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String tekst = jTextArea1.getText();
       int red = jTableUlogovanikorisnici.getSelectedRow();
       if(red!=-1){
           ModelTabeelKorisnik mtk = (ModelTabeelKorisnik) jTableUlogovanikorisnici.getModel();
           Korisnik primalac = mtk.getLista().get(red);
           Korisnik posiljalac= k;
           Date datum = new Date();
           
          Poruka p = new Poruka(-1, posiljalac, primalac, tekst, datum);
          KlijentskiZahtev kz = new KlijentskiZahtev(Operacija.KREIRAJ_PORUKU, p);
          Komunikacija.getInstance().posaljiZahtev(kz);
           
           jTextArea1.setText("");
           
       }
       
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

