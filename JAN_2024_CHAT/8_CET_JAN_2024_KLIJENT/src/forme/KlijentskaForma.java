    public void prikaziPoruke(List<Poruka> poruke) {
        ModelTabelePoruka mtp = new ModelTabelePoruka(poruke);
        jTablePoruke.setModel(mtp);
        Poruka p = poruke.get(0);
        String str = p.getPosiljalac().getKorisnickoIme()+": "+p.getTekst();
        jTextAreaPoslednjaPoruka.setText(str);
    }
